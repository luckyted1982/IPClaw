export async function dataipTradingAgentChat(messages, stream = false) {
  try {
    const messagesWithSystem = [
      { role: 'system', content: DATAIP_TRADING_SYSTEM_PROMPT },
      ...messages,
    ];

    const response = await fetch(`${DEEPSEEK_BASE_URL}/chat/completions`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: DEEPSEEK_MODEL,
        messages: messagesWithSystem,
        temperature: 0.7,
        stream: stream,
        max_tokens: 4096,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('[DataIPTrading] DeepSeek API error:', response.status, errorText);

      const fallbackContent = generateFallbackResponse(messages);
      if (stream) {
        return createStringStream(fallbackContent);
      }
      return fallbackContent;
    }

    if (stream) {
      return response.body;
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      return generateFallbackResponse(messages);
    }

    const cleanContent = content.replace(
      /<\|[\s]*\|[\s]*DSML[\s]*\|[\s]*tool_calls\|>[\s\S]*?<\|[\s]*\|[\s]*DSML[\s]*\|[\s]*tool_calls\|>/g, ''
    ).trim();

    return cleanContent || generateFallbackResponse(messages);

  } catch (error) {
    console.error('[DataIPTrading] Agent error:', error.message);

    const fallbackContent = generateFallbackResponse(messages);
    if (stream) {
      return createStringStream(fallbackContent);
    }
    throw error;
  }
}

