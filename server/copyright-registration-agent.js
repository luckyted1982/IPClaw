/**
 * 版权登记智能体对话 - Workflow驱动模式
 * 设计思路：不依赖外部API，基于专业知识库+Workflow实现智能分析
 */
export async function copyrightRegistrationAgentChat(messages, stream = false) {
  try {
    const messagesWithSystem = [
      { role: 'system', content: COPYRIGHT_REGISTRATION_SYSTEM_PROMPT },
      ...messages,
    ];

    const response = await fetch(`${DEEPSEEK_BASE_URL}/chat/completions`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: DEEPSEEK_MODEL,
        messages: messagesWithSystem,
        temperature: 0.7,
        stream: stream,
        max_tokens: 4096,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('[CopyrightRegistration] DeepSeek API error:', response.status, errorText);
      
      const fallbackContent = generateFallbackResponse(messages);
      if (stream) {
        return createStringStream(fallbackContent);
      }
      return fallbackContent;
    }

    if (stream) {
      return response.body;
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      return generateFallbackResponse(messages);
    }

    const cleanContent = content.replace(
      /<\|[\s]*\|[\s]*DSML[\s]*\|[\s]*tool_calls\|>[\s\S]*?<\|[\s]*\|[\s]*DSML[\s]*\|[\s]*tool_calls\|>/g, ''
    ).trim();

    return cleanContent || generateFallbackResponse(messages);

  } catch (error) {
    console.error('[CopyrightRegistration] Agent error:', error.message);
    
    const fallbackContent = generateFallbackResponse(messages);
    if (stream) {
      return createStringStream(fallbackContent);
    }
    throw error;
  }
}

function generateFallbackResponse(messages) {
  const lastUserMsg = messages.filter(m => m.role === 'user').pop();
  const queryText = lastUserMsg?.content || '';
  
  const nameMatch = queryText.match(/['"]([^'"]+)['"]|登记(.+?)这个|作品[：:]\s*(.+?)(?:的|$)/) || [];
  const workName = nameMatch[1] || nameMatch[2] || nameMatch[3] || '该作品';

  return `# 版权登记分析报告（离线模式）

## 基本信息
- 作品名称：${workName}
- 分析时间：${new Date().toLocaleDateString('zh-CN')}
- 注：当前处于离线分析模式，以下为基于专业规则的初步评估

## 一、作品类型初步判断
请确认"${workName}"的作品类型：
- 📝 **文字作品**（小说、论文、文档、剧本）
- 🎨 **美术作品**（设计图、插画、LOGO、书法）
- 📷 **摄影作品**（照片、图片素材）
- 🎬 **影视作品**（视频、宣传片、动画）
- 🎵 **音乐作品**（歌曲、配乐、音效）
- 💻 **软件作品**（APP、系统、工具软件）

## 二、登记基本条件核查
根据《著作权法》(2020修订)，作品登记需满足：
1. ✅ 属于文学、艺术和科学领域内能以一定形式表现的智力成果
2. ✅ 具有独创性（体现作者的个性表达）
3. ✅ 可以以某种形式复制

## 三、下一步行动建议

### 3.1 请补充以下信息以便更精准分析：
1. 该作品的**具体类型**是什么？
2. **完成时间**是什么时候？
3. **作者/权利人**是谁？（个人/公司）
4. 是否涉及**AI辅助创作**？
5. 是否需要**加急办理**？

### 3.2 推荐登记渠道

| 渠道 | 网址 | 用途 |
|------|------|------|
| **中国版权保护中心** | www.ccopyright.com.cn | 官方登记入口（必用！） |
| **作品登记系统** | register.ccopyright.com.cn | 在线填报提交 |
| **软件登记系统** | register.ccopyright.com.cn/rsoft | 软著专用通道 |

### 3.3 费用参考（2025年标准）
- 文字作品：100元/件
- 美术作品：300元/件  
- 软件著作权：250元/件
- 加急服务：800-1500元（3-5工作日）

---
*由 IPClaw-CopyrightReg 版权登记智能体生成（离线模式）*
*建议连接在线服务获取完整的AI分析能力*`;
}

function createStringStream(text) {
  return Readable.from([text]);
}

