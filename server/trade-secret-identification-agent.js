/**
 * 商业秘密识别定密智能体对话 - Workflow驱动模式
 * 
 * 设计思路：
 * 1. 不使用Function Calling / Tools（纯对话模式）
 * 2. 结构化System Prompt驱动Workflow
 * 3. 支持流式输出（stream=true时返回ReadableStream）
 * 4. 基于DeepSeek强大的推理能力完成全部分析工作
 */
export async function tradeSecretIdentificationAgentChat(messages, stream = false) {
  try {
    // 构建消息历史
    const messagesWithSystem = [
      { role: 'system', content: TRADE_SECRET_IDENTIFICATION_SYSTEM_PROMPT },
      ...messages,
    ];

    // 调用DeepSeek API
    const response = await fetch(`${DEEPSEEK_BASE_URL}/chat/completions`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: DEEPSEEK_MODEL,
        messages: messagesWithSystem,
        temperature: 0.7,
        stream: stream,
        max_tokens: 4096,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('[TradeSecretIdentification] DeepSeek API error:', response.status, errorText);
      
      // 返回降级响应
      const fallbackContent = generateFallbackResponse(messages);
      if (stream) {
        return createStringStream(fallbackContent);
      }
      return fallbackContent;
    }

    // 流式模式：直接透传ReadableStream
    if (stream) {
      return response.body;
    }

    // 非流式模式：解析完整响应
    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      return generateFallbackResponse(messages);
    }

    return content.trim() || generateFallbackResponse(messages);

  } catch (error) {
    console.error('[TradeSecretIdentification] Agent error:', error.message);
    
    const fallbackContent = generateFallbackResponse(messages);
    if (stream) {
      return createStringStream(fallbackContent);
    }
    throw error;
  }
}

/**
 * 生成降级响应（当API调用失败时的后备方案）
 */
function generateFallbackResponse(messages) {
  const lastUserMsg = messages.filter(m => m.role === 'user').pop();
  const queryText = lastUserMsg?.content || '';
  
  const infoMatch = queryText.match(/['"]([^'"]+)['"]|分析(.+?)这个|评估(.+?)的/) || [];
  const infoName = infoMatch[1] || infoMatch[2] || infoMatch[3] || '该信息';

  return `# 商业秘密识别定密报告（离线模式）

## 基本信息
- 评估对象：${infoName}
- 评估时间：${new Date().toLocaleDateString('zh-CN')}
- 注：当前处于离线分析模式，以下为基于专业规则的初步评估

## 一、三要件快速筛查

### 1.1 不为公众所知悉
基于《反不正当竞争法》第9条第4款进行初步判断：
- 该信息是否为本领域一般人员所知悉？需进一步确认
- 能否通过观察上市产品获得？需结合具体情况分析
- 是否已在公开渠道披露？建议检索专利/论文/公开资料

### 1.2 商业价值评估
- 该信息对企业经营是否具有实际或潜在价值？待评估
- 泄露是否会导致竞争优势丧失？需量化分析

### 1.3 保密措施检查
- 是否签订保密协议？
- 是否有分级访问控制？
- 是否采取加密等技术手段？

## 二、初步建议

### 请补充以下信息以便精准定密：
1. 该信息的**具体内容和用途**是什么？
2. 目前采取了哪些**保密措施**？
3. 该信息如果泄露，会造成什么**具体损失**？
4. 所属**行业领域**？（如软件/制药/制造等）

## 三、密级参考标准

| 密级 | 适用场景 | 示例 |
|------|---------|------|
| 🔴 绝密 | 核心技术/独家配方 | 源代码/配方/算法 |
| 🟠 机密 | 重要客户/未发布产品 | 客户名单/设计文档 |
| 🟡 秘密 | 内部流程/一般文档 | 操作手册/会议纪要 |
| 🟢 内部公开 | 内部制度/一般信息 | 员工手册/内部通知 |

---
*由 IPClaw-Identify 商业秘密识别定密智能体生成（离线模式）*
*建议连接在线服务获取完整的AI分析能力*`;
}

/**
 * 将字符串转换为可读流（用于降级场景下的流式响应）
 */
function createStringStream(text) {
  return Readable.from([text]);
}

