export async function tradeSecretProtectionAgentChat(messages, stream = false) {
  try {
    const messagesWithSystem = [
      { role: 'system', content: TRADE_SECRET_PROTECTION_SYSTEM_PROMPT },
      ...messages,
    ];

    const response = await fetch(`${DEEPSEEK_BASE_URL}/chat/completions`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: DEEPSEEK_MODEL,
        messages: messagesWithSystem,
        temperature: 0.7,
        stream: stream,
        max_tokens: 4096,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('[TradeSecretProtection] DeepSeek API error:', response.status, errorText);
      
      const fallbackContent = generateFallbackResponse(messages);
      if (stream) {
        return createStringStream(fallbackContent);
      }
      return fallbackContent;
    }

    if (stream) {
      return response.body;
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      return generateFallbackResponse(messages);
    }

    return content.trim() || generateFallbackResponse(messages);

  } catch (error) {
    console.error('[TradeSecretProtection] Agent error:', error.message);
    
    const fallbackContent = generateFallbackResponse(messages);
    if (stream) {
      return createStringStream(fallbackContent);
    }
    throw error;
  }
}

